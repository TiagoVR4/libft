long	ft_atol(const char *str)
{
	int		i;
	long	r;
	int		signal;
	char	*s;

	signal = 1;
	r = 0;
	i = 0;
	s = (char *)str;
	while (s[i] == ' ' || (s[i] >= 9 && s[i] <= 13))
	{
		i++;
	}
	if (s[i] == '-' || s[i] == '+')
	{
		if (s[i] == '-')
			signal = (-1);
		i++;
	}
	while (s[i] >= '0' && s[i] <= '9')
	{
		r = r * 10 + (s[i] - '0');
		i++;
	}
	return (r * signal);
}
